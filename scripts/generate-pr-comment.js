#!/usr/bin/env node

/**
 * Parses report.json and generates a compact Markdown comment suitable for a GitHub PR.
 * Usage: node scripts/generate-pr-comment.js report.json > pr-comment.md
 */

const fs = require('node:fs');

function main() {
  const reportPath = process.argv[2];
  if (!reportPath) {
    process.stderr.write('Usage: node scripts/generate-pr-comment.js <report.json>\n');
    process.exit(1);
  }

  const report = JSON.parse(fs.readFileSync(reportPath, 'utf8'));
  const sum = report.summary;

  let md = `## WordPress Plugin Compliance Scan\n\n`;
  
  if (sum.errors === 0 && sum.warnings === 0) {
    md += `✅ **Pass:** No critical issues detected! (${sum.executed_rules} rules checked)\n`;
    process.stdout.write(md);
    return;
  }

  md += `🚨 **Needs Attention:** The scanner found ${sum.errors} errors and ${sum.warnings} warnings.\n\n`;

  const criticalFindings = report.findings.filter(f => f.severity === 'error' || f.severity === 'warning');

  md += `| Rule | Severity | Issue | Fix |\n`;
  md += `|---|---|---|---|\n`;

  criticalFindings.forEach(f => {
    const sevIcon = f.severity === 'error' ? '🔴' : '🟡';
    const cleanFix = f.fix ? f.fix.replace(/\n/g, ' ') : 'N/A';
    md += `| \`${f.rule_id}\` | ${sevIcon} ${f.severity} | ${f.title} | ${cleanFix} |\n`;
  });

  md += `\n*Scan generated by the [WP Plugin Compliance Checker](https://github.com/jeewandaniel/wp-plugin-compliance-checker).*`;
  
  process.stdout.write(md);
}

try {
  main();
} catch (e) {
  process.stderr.write(`Failed to generate PR comment: ${e.message}\n`);
  process.exit(1);
}
